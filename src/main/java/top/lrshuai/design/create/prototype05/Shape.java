package top.lrshuai.design.create.prototype05;

public abstract class Shape implements Cloneable{
    private Integer id;
    public String name;
    abstract  void draw();

    public void setId(Integer id) {
        this.id = id;
    }

    public Integer getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public Object clone() {
        Object clone = null;
        try {
            clone = super.clone();
        } catch (CloneNotSupportedException e) {
            e.printStackTrace();
        }
        return clone;
    }
}
